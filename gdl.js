/**
 * Order of operation ENUMs.
 */
Blockly.GDL.ORDER_ATOMIC = 0;         // 0 "" ...

/**
 * Allow for switching between one and zero based indexing, one based by
 * default.
 */
Blockly.GDL.ONE_BASED_INDEXING = true;

/**
 * Initialise the database of variable names.
 * @param {!Blockly.Workspace} workspace Workspace to generate code from.
 */
Blockly.GDL.init = function(workspace) {
  // Create a dictionary of definitions to be printed before the code.
  Blockly.GDL.definitions_ = Object.create(null);
  // Create a dictionary mapping desired function names in definitions_
  // to actual function names (to avoid collisions with user functions).
  Blockly.GDL.functionNames_ = Object.create(null);

  if (!Blockly.GDL.variableDB_) {
    Blockly.GDL.variableDB_ =
        new Blockly.Names(Blockly.GDL.RESERVED_WORDS_);
  } else {
    Blockly.GDL.variableDB_.reset();
  }

  var defvars = [];
  var variables = Blockly.Variables.allVariables(workspace);
  if (variables.length) {
    for (var i = 0; i < variables.length; i++) {
      defvars[i] = Blockly.GDL.variableDB_.getName(variables[i],
          Blockly.Variables.NAME_TYPE);
    }
    Blockly.GDL.definitions_['variables'] =
        'var ' + defvars.join(', ') + ';';
  }
};

/**
 * Prepend the generated code with the variable definitions.
 * @param {string} code Generated code.
 * @return {string} Completed code.
 */
Blockly.GDL.finish = function(code) {
  // Convert the definitions dictionary into a list.
  var definitions = [];
  for (var name in Blockly.GDL.definitions_) {
    definitions.push(Blockly.GDL.definitions_[name]);
  }
  // Clean up temporary data.
  delete Blockly.GDL.definitions_;
  delete Blockly.GDL.functionNames_;
  Blockly.GDL.variableDB_.reset();
  return definitions.join('\n\n') + '\n\n\n' + code;
};

/**
 * Naked values are top-level blocks with outputs that aren't plugged into
 * anything.  A trailing semicolon is needed to make this legal.
 * @param {string} line Line of generated code.
 * @return {string} Legal line of code.
 */
Blockly.GDL.scrubNakedValue = function(line) {
  return line + '\n';
};

/**
 * Encode a string as a properly escaped GDL string, complete with
 * quotes.
 * @param {string} string Text to encode.
 * @return {string} GDL string.
 * @private
 */
Blockly.GDL.quote_ = function(string) {
  // Can't use goog.string.quote since Google's style guide recommends
  // JS string literals use single quotes.
  string = string.replace(/\\/g, '\\\\')
                 .replace(/\n/g, '\\\n')
                 .replace(/'/g, '\\\'');
  return '\'' + string + '\'';
};

/**
 * Common tasks for generating GDL from blocks.
 * Handles comments for the specified block and any connected value blocks.
 * Calls any statements following this block.
 * @param {!Blockly.Block} block The current block.
 * @param {string} code The GDL code created for this block.
 * @return {string} GDL code with comments and subsequent blocks added.
 * @private
 */
Blockly.GDL.scrub_ = function(block, code) {
  var commentCode = '';
  // Only collect comments for blocks that aren't inline.
  if (!block.outputConnection || !block.outputConnection.targetConnection) {
    // Collect comment for this block.
    var comment = block.getCommentText();
    comment = Blockly.utils.wrap(comment, this.COMMENT_WRAP - 3);
    if (comment) {
      if (block.getProcedureDef) {
        // Use a comment block for function comments.
        commentCode += '/**\n' +
                       Blockly.GDL.prefixLines(comment + '\n', ' * ') +
                       ' */\n';
      } else {
        commentCode += Blockly.GDL.prefixLines(comment + '\n', '// ');
      }
    }
    // Collect comments for all value arguments.
    // Don't collect comments for nested statements.
    for (var i = 0; i < block.inputList.length; i++) {
      if (block.inputList[i].type == Blockly.INPUT_VALUE) {
        var childBlock = block.inputList[i].connection.targetBlock();
        if (childBlock) {
          var comment = Blockly.GDL.allNestedComments(childBlock);
          if (comment) {
            commentCode += Blockly.GDL.prefixLines(comment, '// ');
          }
        }
      }
    }
  }
  var nextBlock = block.nextConnection && block.nextConnection.targetBlock();
  var nextCode = Blockly.GDL.blockToCode(nextBlock);
  return commentCode + code + nextCode;
};
